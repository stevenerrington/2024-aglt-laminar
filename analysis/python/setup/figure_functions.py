import scipy
import numpy as np
import matplotlib.pyplot as plt

# ----------------------------------------------------------------------------------------------
#  /////////////////////////////////////////////////////////////////////////////////////////////
# ----------------------------------------------------------------------------------------------
def plt_mean_sem(xvalue, array, color):
    mean = np.nanmean(array, axis=0)

    sem_plus = mean + scipy.stats.sem(array, axis=0, nan_policy = 'omit')
    sem_minus = mean - scipy.stats.sem(array, axis=0, nan_policy = 'omit')

    plt.fill_between(xvalue, sem_plus, sem_minus, color=color, alpha=0.3)
    plt.plot(xvalue,mean, color=color)
    
